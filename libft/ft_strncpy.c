void	ft_strncpy(char *dest, char *src, int len)
{
	int		i;

	i = 0;
	while (i < len)
	{
		if (src)
			dest[i] = src[i];
		else
			dest[i] = 0;
		i++;
	}
	dest[i] = 0;
}
