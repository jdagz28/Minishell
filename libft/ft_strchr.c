#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	l;
	int		i;

	l = c;
	i = 0;
	while (s[i])
	{
		if (s[i] == l)
			return ((char *)&s[i]);
		i++;
	}
	if (l == 0)
		return ((char *)&s[i]);
	return (NULL);
}
